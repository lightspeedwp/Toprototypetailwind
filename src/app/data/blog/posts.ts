/**
 * Get blog post by slug.
 */
export function getBlogPost(slug: string): BlogPost | undefined {
  return BLOG_POSTS.find(p => p.slug === slug);
}

/**
 * Get blog posts by author slug.
 */
export function getBlogPostsByAuthor(authorNameSlug: string): BlogPost[] {
  // Simple conversion for demo purposes: "sarah-mitchell" -> "Sarah Mitchell"
  const name = authorNameSlug.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  return BLOG_POSTS.filter(p => p.author === name);
}

/**
 * Get FAQs by category slug.
 */
export function getFAQsByCategory(categorySlug: string): FAQ[] {
  return FAQS.filter(faq => faq.categories.includes(categorySlug));
}

/**
 * Get featured FAQs.
 */
export function getFeaturedFAQs(): FAQ[] {
  return FAQS.filter(faq => faq.featured);
}

/**
 * Get FAQs for a specific content type.
 */
export function getFAQsForContentType(contentType: string): FAQ[] {
  return FAQS.filter(faq => faq.relatedContentType === contentType);
}

/**
 * Search FAQs by query string.
 */
export function searchFAQs(query: string): FAQ[] {
  const lower = query.toLowerCase();
  return FAQS.filter(
    faq =>
      faq.question.toLowerCase().includes(lower) ||
      faq.answer.toLowerCase().includes(lower) ||
      faq.excerpt.toLowerCase().includes(lower)
  );
}
